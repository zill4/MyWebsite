<script>
import { curRoute } from './router.js';

export let page = {
    path: 'home',
    name: 'Home'
}

function redirectTo(event) {
    // change current router path

    cureRoute.set(event.target.pathName);

    // push the path into the web browser history API

    window.history.pushState({path: page.path}, '', window.location.origin + page.path);
}

</script>

<style>

    a {
        text-transform: uppercase;

        text-decoration: underline;

        padding: 1rem;
    }

</style>


    <a href={page.path} on:click|preventDefault={redirectTo}>
    {page.name}
    </a>
    